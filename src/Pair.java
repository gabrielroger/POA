public class Pair {
    private int move;
    private int score;
    public int getMove() {
        return move;
    }
    public void setMove(int move) {
        this.move = move;
    }
    public int getScore() {
        return score;
    }
    public void setScore(int score) {
        this.score = score;
    }
    public Pair(int move, int score) {
        this.move = move;
        this.score = score;
    }


}
